a = 2
b = 3

# + operator used for addition
print(a + b)

# - operator used for subtraction
print(a - b)

# (*) operator used for multiplication
print(a * b)

# / operator used for division
print(a / b)

# % operator used for finding remainder
print(a % b)

# (//) operator used for getting floor division
print(a // b)

# (**) operator used for finding exponential
print(a**b)

# applying opertaor
print(~b, a & b, a >> b, b << a)
